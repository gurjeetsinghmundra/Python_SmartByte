L1=[1,8,22,7,2,21,15]


L1.sort()

print(L1)

# Sorts the list in ascending order

L1.reverse()
print(L1)

# Reverses the list

L1.append(99)
print(L1)

# Appends 99 to the end of the list

L1.insert(3,55)
print(L1)
# Inserts 55 at index 3

L1.pop(2)
print(L1)

# Removes element at index 2

L1.remove(21)
print(L1)